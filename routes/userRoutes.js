/**
 * @swagger
 * /users/notification-settings:
 *   patch:
 *     summary: Update user notification settings
 *     description: Update notification preferences for the authenticated user
 *     tags: [Users]
 *     security:
 *       - bearerAuth: []
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             properties:
 *               eventUpdates:
 *                 type: boolean
 *                 description: Receive event update notifications
 *                 example: true
 *               eventReminders:
 *                 type: boolean
 *                 description: Receive event reminder notifications
 *                 example: true
 *               weatherAlerts:
 *                 type: boolean
 *                 description: Receive weather alert notifications
 *                 example: true
 *               pushNotifications:
 *                 type: boolean
 *                 description: Enable push notifications
 *                 example: true
 *     responses:
 *       200:
 *         description: Notification settings updated successfully
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 status:
 *                   type: string
 *                   example: "success"
 *                 data:
 *                   type: object
 *                   properties:
 *                     notificationSettings:
 *                       type: object
 *                       properties:
 *                         eventUpdates:
 *                           type: boolean
 *                         eventReminders:
 *                           type: boolean
 *                         weatherAlerts:
 *                           type: boolean
 *                         pushNotifications:
 *                           type: boolean
 *       400:
 *         description: Invalid input data or no valid settings provided
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 *       401:
 *         description: Not authorized
 *         content:
 *           application/json:
 *             schema:
 *               $ref: '#/components/schemas/Error'
 */

// Public routes
router.post('/register', register);
router.post('/login', login);
router.post('/request-otp', passwordResetLimiter, requestOTP);
router.post('/reset-password', passwordResetLimiter, resetPassword);

// Protected routes
router.use(protect);
router.get('/profile', userCache(600), getProfile); // Cache for 10 minutes
router.patch('/profile', invalidateCache(['user:.*:profile']), updateProfile);
router.patch('/avatar', uploadAvatar, invalidateCache(['user:.*:profile']), updateAvatar);
router.post('/fcm-token', invalidateCache(['user:.*:profile']), updateFCMToken);
router.patch('/notification-settings', invalidateCache(['user:.*:profile']), updateNotificationSettings);

module.exports = router; 